function getThemeKey(prompt: string): keyof typeof EMOJIS {
  const p = prompt.toLowerCase();
  if (p.includes('nature') || p.includes('animal')) return 'nature';
  if (p.includes('technology') || p.includes('computer')) return 'tech';
  if (p.includes('space') || p.includes('galaxy')) return 'space';
  if (p.includes('food') || p.includes('fruit')) return 'food';
  return 'nature';
}

export async function fetchThemeEmojis(prompt: string, count: number): Promise<string[]> {
  // Giả lập độ trễ cực ngắn để UI mượt mà hơn
  await new Promise(resolve => setTimeout(resolve, 300));

  const key = getThemeKey(prompt);
  const pool = EMOJIS[key];
  
  // Xáo trộn danh sách có sẵn
  const shuffled = [...pool].sort(() => 0.5 - Math.random());
  
  // Trả về số lượng biểu tượng cần thiết
  return shuffled.slice(0, count);
}
